public class TimeIs {
    public static void main(String[] args) {
        String message = "Times is: ";
        int hour = 11;
        int minute = 59;
        System.out.print(message);
        System.out.print(hour);
        System.out.print(" : ");
        System.out.print(minute);
        System.out.print("\nTime: ");
        System.out.println(hour * 60 + minute);

    }
}
