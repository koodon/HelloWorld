public class DoublePrecision {
    public static void DPrecision() {
        double pi;
        pi = 3.14159;
        System.out.print("Pi = ");
        System.out.println(pi);
        double minute = 59.0;
        System.out.print("Fraction of the hour that has passed: ");
        System.out.println(minute / 60.);
        double x = 1.0 / 3.0;
        System.out.print("1 / 3 = ");
        System.out.println(x);
    }
}
